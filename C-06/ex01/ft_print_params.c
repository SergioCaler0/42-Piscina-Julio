#include <unistd.h>

int	main(int argc, char **argv)
{
	int	f;
	int	c;

	f = 0;
	c = 0;
	while (argc > 1)
	{
		c = 1;
		while (c < argc)
		{
			while (argv[c][f] != '\0')
			{
				write(1, &argv[c][f], 1);
				f++;
			}
			f = 0;
			write(1, "\n", 1);
			c++;
		}
		return (0);
	}
}
