#include <unistd.h>

int	main(int argc, char **argv)
{
	int	f;

	f = 0;
	(void) argc;
	while (argv[0][f] != '\0')
	{
		write(1, &argv[0][f], 1);
		f++;
	}
	write(1, "\n", 1);
}
