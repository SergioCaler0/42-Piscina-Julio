#include <stdio.h>

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (src[i] != '\0' && i < n)
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}

int main()
{
	char destino[15];
    char origen[] = "lista o";
    int n = 15;

    printf("%s\n",ft_strncpy(destino, origen, n));
	printf("%s\n", destino);
}
